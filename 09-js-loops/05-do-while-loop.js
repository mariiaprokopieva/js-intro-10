let i = 1, j = 1;
while(i <= 3) { // false
    console.log('This is while loop block');
    i++;
}
do {
    console.log('This is do-while loop block');
    j++;
} while(j <= 3);


let k = 1;

while (k > 5) {
    console.log('This is while loop block', k);
}

do {
    console.log('This is do-while loop block', k);
} while (k > 5);

